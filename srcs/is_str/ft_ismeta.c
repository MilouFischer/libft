#include "libft.h"

int		ft_ismeta(int c)
{
	int		ret;

	ret = FALSE;
	if (c == '|' || c == '&' || c == ';' || c == '(' || c == ')'
		|| c == '<' || c == '>')
	{
		ret = TRUE;
	}
	return (ret);
}
