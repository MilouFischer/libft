#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char			*cpy;
	unsigned int	i;

	i = 0;
	if (!s1)
		return (NULL);
	if (!(cpy = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1))))
		return (NULL);
	while (s1[i])
	{
		cpy[i] = s1[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
